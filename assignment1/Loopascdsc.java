package assignment1;

public class Loopascdsc {

	public static void main(String[] args) {
		System.out.println("number from 1 t0 10 in ascending form:");
	 for(int i=1;i<=10;i++) {
		 System.out.println(i);
		 
	 }
	 System.out.println("number from 1 t0 10 in descending form:");
	 for(int j=10;j>=1;j--) {
		 System.out.println(j);
	 }

	}

}
